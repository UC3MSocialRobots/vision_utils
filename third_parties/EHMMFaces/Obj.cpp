#include "Obj.h"
#include <assert.h>

using std::string;

//*****************************************************************************

Obj::Obj( ) : _name("")
{
}

//*****************************************************************************

Obj::~Obj( )
{
}

//*****************************************************************************

void Obj::Create( const string &userName )
{
	assert( userName != "" );
		
	_name = userName;
}

//*****************************************************************************

const string& Obj::GetName( ) const
{
	assert( _name != "" );

	return _name;
}

